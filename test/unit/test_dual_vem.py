    def test_dual_vem_1d_iso(self):
        g = pp.structured.CartGrid(3, 1)
        g.compute_geometry()

        kxx = np.ones(g.num_cells)
        perm = pp.SecondOrderTensor(3, kxx, kyy=1, kzz=1)
        bf = g.tags["domain_boundary_faces"].nonzero()[0]
        bc = pp.BoundaryCondition(g, bf, bf.size * ["dir"])

        solver = pp.DualVEM(physics="flow")

        param = pp.Parameters(g)
        param.set_tensor(solver, perm)
        param.set_bc(solver, bc)
        M = solver.matrix(g, {"param": param}).todense()

        M_known = 1e-2 * np.array(
            [
                [25, -(8 + 1 / 3.), 0, 0, 1e2, 0, 0],
                [-(8 + 1 / 3.), 50, -(8 + 1 / 3.), 0, -1e2, 1e2, 0],
                [0, -(8 + 1 / 3.), 50, -(8 + 1 / 3.), 0, -1e2, 1e2],
                [0, 0, -(8 + 1 / 3.), 25, 0, 0, -1e2],
                [1e2, -1e2, 0, 0, 0, 0, 0],
                [0, 1e2, -1e2, 0, 0, 0, 0],
                [0, 0, 1e2, -1e2, 0, 0, 0],
            ]
        )

        rtol = 1e-15
        atol = rtol
        assert np.allclose(M, M.T, rtol, atol)
        assert np.allclose(M, M_known, rtol, atol)

