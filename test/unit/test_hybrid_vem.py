class BasicsTest(unittest.TestCase):

    # ------------------------------------------------------------------------------#

    def test_dual_hybrid_vem_1d_iso(self):
        g = structured.CartGrid(3, 1)
        g.compute_geometry()

        kxx = np.ones(g.num_cells)
        perm = tensor.SecondOrderTensor(g.dim, kxx)
        bf = g.tags["domain_boundary_faces"].nonzero()[0]
        bc = BoundaryCondition(g, bf, bf.size * ["neu"])

        solver = hybrid.HybridDualVEM(physics="flow")

        param = Parameters(g)
        param.set_tensor(solver, perm)
        param.set_bc(solver, bc)
        M = solver.matrix_rhs(g, {"param": param})[0].todense()

        M_known = 3 * np.array(
            [[-1, 1, 0, 0], [1, -2, 1, 0], [0, 1, -2, 1], [0, 0, 1, -1]]
        )

        rtol = 1e-15
        atol = rtol
        assert np.allclose(M, M.T, rtol, atol)
        assert np.allclose(M, M_known, rtol, atol)

